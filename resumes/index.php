                                <div class="row">
                                    <div class="col-md-12">
                                        <p>　　如果稍微看過關於我，會知道我唸過三間大學，三個似曾相似的科系但差異卻很大。
                                        有時候人生總是經歷過一些事情，所以才會想不斷轉學，因為想要到更好的地方，想要去真正有未來發展的地方。</p>
                                        <p>　　從一個小小的高職生，先是透過技優，再來有過雙主修的經歷後。最後決定轉學，只因為自己不喜歡太安逸的地方。
                                        坦白說，我是個畢業於南部高職的夜校生。在各種資源被日校吸收外，也因為夜校多數的老師根本不瞧不起自己的學生，
                                        所以對於真正想唸書的人相當不利，也正因如此我選擇了一條不是高職生會到的地方，也就是文化大學。</p>
                                        <p>　　雖然在我的第一間學校沒有念到畢業，但從我進去的那一刻起，我時時刻刻就想要往更好的地方去。首次轉學考其實是升大二的時候，
                                        但是那次因為準備不夠，所以沒有轉成功。因此首次轉學是升二下的寒假。而到了第二間學校，在這裡也僅有短短的一個學期，
                                        就選擇再次轉學，因為真的很不喜歡整天無所事事的虛度光陰，不如拚下去到國立學校為未來奮鬥！</p>
                                        <p>　　我是個願意嘗試各種機會的人，在求學生涯中，我抱持的「好還要更好」的心態。
                                            即使現在有個不算差的學校，還是會努力去試著要到更好的學校去。我喜歡挑戰自己的能力在哪，因為沒有試過就不會知道自己哪裡比別人差。
                                            知道自己不足的地方後，想辦法努力加強自己不足的地方，去加強、去改進，並再一次證明自己辦的到。</p>
